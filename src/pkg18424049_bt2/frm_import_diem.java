    private void btnChooseFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChooseFileActionPerformed
        JFileChooser frmFileChoose = new JFileChooser("./filecsv");
        frmFileChoose.setDialogTitle("Chọn file nhập liệu");
        FileNameExtensionFilter filterExtension = new FileNameExtensionFilter("Các tập tin *.csv", "csv");
        frmFileChoose.setFileFilter(filterExtension);
        int flag = frmFileChoose.showOpenDialog(null);
        if (flag == JFileChooser.APPROVE_OPTION) {

            File f = frmFileChoose.getSelectedFile();
            txtPath.setText(f.getAbsolutePath());

            String[] columns = new String[]{"Mã sinh viên", "Điểm GK", "Điểm CK", "Điểm khác", "Điểm tổng"};
            DefaultTableModel modelSt = new DefaultTableModel(null, columns);
            // load danh sach
            String path = txtPath.getText().trim();
            ArrayList<String[]> lines = FileHelper.ReadFile(path);
            if (lines == null) {

            } else {
                for (int i = 0; i < lines.size(); i++) {
                    modelSt.addRow(lines.get(i));
                }
            }
            tbSudents.setModel(modelSt);
        }
    }//GEN-LAST:event_btnChooseFileActionPerformed

