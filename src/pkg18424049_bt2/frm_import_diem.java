    private void btnImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportActionPerformed
        String stClass = cbClass.getSelectedItem().toString();
        String mamh = cbCourse.getSelectedItem().toString().split("-")[0].trim();

        String path = txtPath.getText().trim();
        StringBuilder sb = new StringBuilder();
        ArrayList<String[]> importSuccess = new ArrayList<String[]>();
        ArrayList<String[]> lines = FileHelper.ReadFile(path);
        for (int i = 0; i < lines.size(); i++) {
            String[] strs = lines.get(i);
            Diem d = new Diem();
            d.setMaSinhVien(strs[0]);
            d.setDiemGk(Float.parseFloat(strs[1]));
            d.setDiemCk(Float.parseFloat(strs[2]));
            d.setDeimKhac(Float.parseFloat(strs[3]));
            d.setDiemTong(Float.parseFloat(strs[4]));
            d.setMaLopTkb(stClass);
            d.setMaMh(mamh);
            d.setKetQua(Float.parseFloat(strs[4]) >= 5 ? "Đậu" : "Rớt");

            String msg = new DiemDAO().createDiem(d);
            sb.append(strs[0]).append(" - ").append(msg).append("\r\n");
            if (msg.equals("Thêm thành công!")) {
                importSuccess.add(strs);
            }
        }

        // load lại ds import thành công
        modelSt.getDataVector().removeAllElements();
        String[] columns = new String[]{"Mã sinh viên", "Điểm GK", "Điểm CK", "Điểm khác", "Điểm tổng"};
        DefaultTableModel modelSt = new DefaultTableModel(null, columns);
        for (int i = 0; i < importSuccess.size(); i++) {
            modelSt.addRow(importSuccess.get(i));
        }
        tbSudents.setModel(modelSt);
        //msg
        JOptionPane.showMessageDialog(null, sb.toString(), "Thông báo", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnImportActionPerformed

